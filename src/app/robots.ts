import type { MetadataRoute } from "next";
import { getSiteUrl } from "@/ai/site-url";

export default function robots(): MetadataRoute.Robots {
  const siteUrl = getSiteUrl();

  return {
    rules: {
      userAgent: "*",
      allow: "/",
      disallow: ["/api/", "/_next/"]
    },
    sitemap: [`${siteUrl}/sitemap.xml`, `${siteUrl}/feed.xml`],
    host: siteUrl
  };
}
